	/**
	Linearly interpolate the position where an isosurface cuts
    an edge between two vertices, each with their own scalar value.
	*/
	static Vec3d vertexInterp(double isolevel, const Vec3c& p1, const Vec3c& p2, double valp1, double valp2)
	{
	   double mu;
	   Vec3d p;

	   if (fabs(isolevel - valp1) < 0.00001)
	      return Vec3d(p1);
	   if (fabs(isolevel - valp2) < 0.00001)
	      return Vec3d(p2);
	   if (fabs(valp1 - valp2) < 0.00001)
	      return Vec3d(p1);
	   mu = (isolevel - valp1) / (valp2 - valp1);
	   p.x = (double)p1.x + mu * (double)(p2.x - p1.x);
	   p.y = (double)p1.y + mu * (double)(p2.y - p1.y);
	   p.z = (double)p1.z + mu * (double)(p2.z - p1.z);

	   return p;
	}

	
	static std::vector<Triangle> polygonise(const GridCell& grid, int cubeindex, double isolevel)
	{
	   
	   Vec3d vertlist[12];

	   /* Find the vertices where the surface intersects the cube */
	   if ((edgeTable[cubeindex] & 1) != 0)
	      vertlist[0] =
	         vertexInterp(isolevel, grid.p0, grid.p1, grid.val0, grid.val1);
	   if ((edgeTable[cubeindex] & 2) != 0)
	      vertlist[1] =
	         vertexInterp(isolevel, grid.p1, grid.p2, grid.val1, grid.val2);
	   if ((edgeTable[cubeindex] & 4) != 0)
	      vertlist[2] =
	         vertexInterp(isolevel, grid.p2, grid.p3, grid.val2, grid.val3);
	   if ((edgeTable[cubeindex] & 8) != 0)
	      vertlist[3] =
	         vertexInterp(isolevel, grid.p3, grid.p0, grid.val3, grid.val0);
	   if ((edgeTable[cubeindex] & 16) != 0)
	      vertlist[4] =
	         vertexInterp(isolevel, grid.p4, grid.p5, grid.val4, grid.val5);
	   if ((edgeTable[cubeindex] & 32) != 0)
	      vertlist[5] =
	         vertexInterp(isolevel, grid.p5, grid.p6, grid.val5, grid.val6);
	   if ((edgeTable[cubeindex] & 64) != 0)
	      vertlist[6] =
	         vertexInterp(isolevel, grid.p6, grid.p7, grid.val6, grid.val7);
	   if ((edgeTable[cubeindex] & 128) != 0)
	      vertlist[7] =
	         vertexInterp(isolevel, grid.p7, grid.p4, grid.val7, grid.val4);
	   if ((edgeTable[cubeindex] & 256) != 0)
	      vertlist[8] =
	         vertexInterp(isolevel, grid.p0, grid.p4, grid.val0, grid.val4);
	   if ((edgeTable[cubeindex] & 512) != 0)
	      vertlist[9] =
	         vertexInterp(isolevel, grid.p1, grid.p5, grid.val1, grid.val5);
	   if ((edgeTable[cubeindex] & 1024) != 0)
	      vertlist[10] =
	         vertexInterp(isolevel, grid.p2, grid.p6, grid.val2, grid.val6);
	   if ((edgeTable[cubeindex] & 2048) != 0)
	      vertlist[11] =
	         vertexInterp(isolevel, grid.p3, grid.p7, grid.val3, grid.val7);

	   // Create the triangle 
	   std::vector<Triangle> triangles;
	   for (size_t i = 0; triTable[cubeindex][i] != -1; i += 3)
	   {
		   Triangle tri;
		   tri.p0 = vertlist[triTable[cubeindex][i  ]];
		   tri.p1 = vertlist[triTable[cubeindex][i+1]];
		   tri.p2 = vertlist[triTable[cubeindex][i+2]];
		   
		   triangles.push_back(tri);
	   }

	   return triangles;
	}
	
	/**
	Calculates surface area of object consisting of pixels with gray value above the given isovalue.
	Note that image borders are not handled specially so pixel value outside the image area is assumed
	to be the same than the value of the nearest pixel inside image area.
	*/
	template<typename pixel_t> double getMarchingCubesArea(Image<pixel_t>& object, double isovalue)
	{
		return 0;

		coord_t width = object.width();
		coord_t height = object.height();
		coord_t depth = object.depth();

		GridCell cell;
		
		double area = 0.0;

		// TODO: Should we use this parallelization or not?
		//#pragma omp parallel for reduction(+:area)
		for(coord_t z = 0; z < depth; z++)
		{
			for(coord_t y = 0; y < height; y++)
			{
				for(coord_t x = 0; x < width; x++)
				{
					// Clamp coordinates to image dimensions.

					cell.p0.x = x;
					cell.p3.x = x;
					cell.p4.x = x;
					cell.p7.x = x;
					
					if(x < width - 1)
					{
						cell.p1.x = x + 1;
						cell.p2.x = x + 1;
						cell.p5.x = x + 1;
						cell.p6.x = x + 1;
					}
					else
					{
						cell.p1.x = x;
						cell.p2.x = x;
						cell.p5.x = x;
						cell.p6.x = x;
					}
					
					cell.p4.y = y;
					cell.p5.y = y;
					cell.p6.y = y;
					cell.p7.y = y;
					
					if(y < height - 1)
					{
						cell.p0.y = y + 1;
						cell.p1.y = y + 1;
						cell.p2.y = y + 1;
						cell.p3.y = y + 1;
					}
					else
					{
						cell.p0.y = y;
						cell.p1.y = y;
						cell.p2.y = y;
						cell.p3.y = y;
					}
					
					cell.p2.z = z;
					cell.p3.z = z;
					cell.p6.z = z;
					cell.p7.z = z;
					
					if(z < depth - 1)
					{
						cell.p0.z = z + 1;
						cell.p1.z = z + 1;
						cell.p4.z = z + 1;
						cell.p5.z = z + 1;
					}
					else
					{
						cell.p0.z = z;
						cell.p1.z = z;
						cell.p4.z = z;
						cell.p5.z = z;
					}
					
					// Change this from "clamp" behavior to "zero outside" behavior if needed.
					cell.val0 = (double)object(cell.p0);
					cell.val1 = (double)object(cell.p1);
					cell.val2 = (double)object(cell.p2);
					cell.val3 = (double)object(cell.p3);
					cell.val4 = (double)object(cell.p4);
					cell.val5 = (double)object(cell.p5);
					cell.val6 = (double)object(cell.p6);
					cell.val7 = (double)object(cell.p7);
					
					
					// Determine the index into the edge table which
					// tells us which vertices are inside of the surface
					int cubeindex = 0;
					if (cell.val0 < isovalue) cubeindex |= 1;
					if (cell.val1 < isovalue) cubeindex |= 2;
					if (cell.val2 < isovalue) cubeindex |= 4;
					if (cell.val3 < isovalue) cubeindex |= 8;
					if (cell.val4 < isovalue) cubeindex |= 16;
					if (cell.val5 < isovalue) cubeindex |= 32;
					if (cell.val6 < isovalue) cubeindex |= 64;
					if (cell.val7 < isovalue) cubeindex |= 128;

					// Cube is entirely in/out of the surface
					if (edgeTable[cubeindex] != 0)
					{
						std::vector<Triangle> triangles = polygonise(cell, cubeindex, isovalue);
					
						for(size_t n = 0; n < triangles.size(); n++)
						{
							Triangle tri = triangles[n];
							area += 0.5 * ((tri.p1 - tri.p0).cross(tri.p2 - tri.p0)).norm();
						}
					}
				}
			}
		}
		
		return area;
	}

